#!/usr/bin/env python3
"""
Auto-annotate your custom images using YOLOv8 and save the result as a
COCO-format ground-truth JSON file.

Strategy
--------
Run YOLOv8n inference at a HIGH confidence threshold (default 0.50) on every
image in data/images/val/.  The resulting detections are saved as COCO-format
bounding-box annotations in data/annotations/instances_custom.json.

This "pseudo-labeling" approach creates a self-consistent dataset:
  • The annotation file correctly reflects what objects are detectable in each image.
  • mAP evaluated against these annotations measures backend / model consistency
    rather than absolute accuracy, which is acceptable for an assignment dataset.
  • Mention this in your report: "annotations generated by YOLOv8n at conf=0.5".

Usage
-----
    # from the backend/ directory (venv must be active):
    python ../scripts/create_custom_annotations.py

    # from project root:
    cd backend && python ../scripts/create_custom_annotations.py && cd ..

    # custom confidence or image dir:
    python ../scripts/create_custom_annotations.py --conf 0.45 --images-dir data/images/val

Output
------
    data/annotations/instances_custom.json   ← COCO-format GT annotations
"""
